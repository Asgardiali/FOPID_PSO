syms s;

if order == 1
    x = ((1+a)*s+(1-a))/((1-a)*s+(1+a));    
elseif order == 2
    x = ((a^2+3*a+2)*s^2+(-2*a^2+8)*s+(a^2-3*a+2))/((a^2-3*a+2)*s^2+(-2*a^2+8)*s+(a^2+3*a+2));

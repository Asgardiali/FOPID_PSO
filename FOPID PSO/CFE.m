    x = ((a^3+6*a^2+11*a+6)*s^3+(-3*a^3-6*a^2+27*a+54)*s^2+(3*a^3-6*a^2-27*a+54)*s+(-a^3+6*a^2-11*a+6))/((-a^3+6*a^2-11*a+6)*s^3+(3*a^3-6*a^2-27*a+54)*s^2+(-3*a^3-6*a^2+27*a+54)*s+(a^3+6*a^2+11*a+6));
elseif order == 4
    x = ((a^4+10*a^3+35*a^2+50*a+24)*s^4+(-4*a^4-20*a^3+40*a^2+320*a+384)*s^3+(6*a^4-150*a^2+864)*s^2+(-4*a^4+20*a^3+40*a^2-320*a+384)*s+(a^4-10*a^3+35*a^2-50*a+24))/((a^4-10*a^3+35*a^2-50*a+24)*s^4+(-4*a^4+20*a^3+40*a^2-320*a+384)*s^3+(6*a^4-150*a^2+864)*s^2+(-4*a^4-20*a^3+40*a^2+320*a+384)*s+(a^4+10*a^3+35*a^2+50*a+24));
end

end

